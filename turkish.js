// Türkçe'ye çeviri fonksiyonu
function translateToTurkish() {
  // Sayfadaki tüm metinleri içeren elementleri seç
  const textElements = document.querySelectorAll('p, h1, h2, h3, h4, h5, h6, a, button, label, span, option');
  
  // Her eleman için çeviri uygula
  textElements.forEach(el => {
    const originalText = el.innerText.trim();
    // Çevirisi varsa uygula
    if (translations[originalText]) {
      el.innerText = translations[originalText];
    }
  });
  
  // Input placeholder ve diğer özellikleri çevir
  document.querySelectorAll('input, textarea, select').forEach(el => {
    if (el.placeholder && translations[el.placeholder]) {
      el.placeholder = translations[el.placeholder];
    }
    if (el.title && translations[el.title]) {
      el.title = translations[el.title];
    }
  });
  
  console.log('Sayfa Türkçe\'ye çevrildi.');
}

// URL parametresi ile çeviri kontrolü
document.addEventListener('DOMContentLoaded', function() {
  // URL'de 'lang=tr' parametresi varsa sayfayı Türkçe'ye çevir
  const urlParams = new URLSearchParams(window.location.search);
  if (urlParams.get('lang') === 'tr') {
    // Sayfanın tam yüklenmesi için kısa bir gecikme
    setTimeout(translateToTurkish, 500);
  }
  
  // Ayrıca tetikleyici URL'ye tıklandığında da çeviri yapılacak
  // Örnek: istediğiniz herhangi bir element/buton/link ile bağlantı kurabilirsiniz
  const triggerElements = document.querySelectorAll('.translate-trigger, #translate-button');
  triggerElements.forEach(el => {
    el.addEventListener('click', function(e) {
      e.preventDefault(); // Eğer bir bağlantı ise, sayfa yönlendirmesini engelle
      translateToTurkish();
    });
  });
});

// Global fonksiyon olarak tanımla (sayfa dışından çağrılabilmesi için)
window.translateToTurkish = translateToTurkish;